public class Main {
    /*
    Desarrolla un programa que calcule el área de un rectángulo.
    Declara variables para la base y la altura, y muestra el resultado del cálculo.
     */
    public static void main (String[] args){
        double base = 3;
        double altura = 5;
        double area = base * altura;
        System.out.println("La base del cuadrado es: " + base);
        System.out.println("La altura del cuadrado es: " + altura);
        System.out.println("El area del cuadrado mide: " + area);
    }
}
